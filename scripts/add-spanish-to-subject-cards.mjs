// Unicode-aware word boundary helpers
// JS \b treats accented chars (é, ñ, ê, etc.) as non-word chars, causing false matches.
// These helpers use lookbehind/lookahead with a broader character class.
const WB = '(?<![a-zA-ZÀ-ÿ])'; // "start of word" boundary
const WE = '(?![a-zA-ZÀ-ÿ])';  // "end of word" boundary

function wb(word) {
  // Create a regex pattern with Unicode-aware word boundaries
  const escaped = word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  return new RegExp(WB + escaped + WE, 'g');
}

